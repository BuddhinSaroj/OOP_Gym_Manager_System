public class Date {

    private int membership_start_date;
    private int membership_start_month;
    private int membership_start_year;

    public Date(int membership_start_date, int membership_start_month, int membership_start_year) {
        this.membership_start_date = membership_start_date;
        this.membership_start_month = membership_start_month;
        this.membership_start_year = membership_start_year;
    }
}
